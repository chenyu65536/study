public class Q69 {
    public int mySqrt(int x) {
        if (x == 1 || x == 0) {
            return x;
        }
        long top = x;
        long bottom = 0;
        while (true) {
            long mid = (top + bottom) / 2;
            if (top - bottom == 1) {
                return (int) mid;
            }
            if (mid * mid > x) {
                top = mid;
            } else {
                bottom = mid;
            }
        }
    }
}
